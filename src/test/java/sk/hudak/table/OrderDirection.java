public enum OrderDirection {

    /**
     * <b>Ascending sort</b> - arranging data from the lowest to the highest
     * in a specified field (for example from A to Z, or from 0 to 9).
     */
    ASC,

    /**
     * <b>Descending sort</b> - arranging data from the highest to the lowest
     * in a specified field (for example from Z to A, or from 9 to 0).
     */
    DESC;
}